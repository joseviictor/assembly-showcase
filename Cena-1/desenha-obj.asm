; -------------------------------------------------------------------------------------------------------------------
; ESCREVE_PIXEL - Escreve um pixel na linha e coluna indicadas.
; Argumentos:   R1 - linha
;               R2 - coluna
;               R3 - cor do pixel (em formato ARGB de 16 bits)
;
; -------------------------------------------------------------------------------------------------------------------
escreve_pixel:
	MOV [DEFINE_LINHA], R1				; seleciona a linha
	MOV [DEFINE_COLUNA], R2				; seleciona a coluna
	MOV [DEFINE_PIXEL], R3				; altera a cor do pixel na linha e coluna selecionadas
	RET

; -------------------------------------------------------------------------------------------------------------------
; ATRASO - Faz DELAY iterações, para implementar um atraso no tempo, de forma não bloqueante.
; Argumentos:   R0 - endereço da variável que guarda o contador do atraso
;				R2 - valor inicial do atraso a repor após o fim do atraso (ATRASO_ANIMACAO_SIMPLES ou ATRASO_ANIMACAO_COMPLEXA)
; Saidas:		R1 - Se 0, o atraso chegou ao fim
; -------------------------------------------------------------------------------------------------------------------
atraso:
	MOV  R1, [R0]						; obtém valor do contador do atraso
	SUB  R1, 1							; Decrementa o contador em 1
	MOV  [R0], R1						; atualiza valor do contador do atraso
	JNZ  sai_atraso						; Se o contador não for zero, salta para `sai_atraso_neve`
	MOV  [R0], R2						; volta a colocar o valor inicial no contador do atraso
sai_atraso:			
	RET

; -------------------------------------------------------------------------------------------------------------------
; MOSTRA_OBJETO: Rotina para exibir um ecrã e reproduzir um efeito sonoro associado ao objeto. 	
; Argumentos: R1 - endereço da 2ª palavra da variável que define o ecrã e estado do objeto (exibido/ocultado)
;			  R2 - número do ecrã a ser exibido/ocultado, em que o objeto está desenhado
; -------------------------------------------------------------------------------------------------------------------
mostra_objeto:
	MOV [MOSTRA_ECRA], R2				; Mostra o ecrã	
	MOV R4, 1							; Define o novo estado como "exibido" (1)
	MOV [R1], R4						; atualiza estado do objeto para exibido (1)
	
define_som_do_objeto:
	MOV R4, 4							; Identificador do som associado ao ecrã do giftbox
	CMP R2, 7							; Verifica se o objeto corresponde ao ecrã 7 (Giftbox)
	JZ reproduz_som						; se o objeto a ser mostrado é o giftbox, reproduz efeito sonoro respetivo
	
	MOV R4, 3							; Identificador do som associado ao ecrã do pai natal
	CMP R2, 6							; Verifica se o objeto corresponde ao ecrã 6 (Pai Natal)
	JZ reproduz_som						; se o objeto a ser mostrado é o pai natal, reproduz efeito sonoro respetivo
	
	MOV R4, 5							; Identificador do som associado ao ecrã da árvore de natal
	CMP R2, 5							; Verifica se o objeto corresponde ao ecrã 5 (Árvore de Natal)
	JZ reproduz_som						; se o objeto a ser mostrado é a árvore, reproduz efeito sonoro respetivo
	
	MOV R4, 6							; Identificador do som associado ao ecrã do letreiro merry xmas
	CMP R2, 2							; Verifica se o objeto corresponde ao ecrã 2 (Letreiro Merry Xmas)
	JZ reproduz_som						; se o objeto a ser mostrado é o letreiro merry xmas, reproduz efeito sonoro respetivo
	
	JMP fim_mostra_objeto				; Finaliza a rotina

reproduz_som:
	MOV [REPRODUZ_SOM], R4				; Reproduz o som
	JMP fim_mostra_objeto				; Finaliza a rotina

fim_mostra_objeto:						; Restaura os valores dos registradores
	RET

; -------------------------------------------------------------------------------------------------------------------
; ESCONDE_OBJETO: Rotina para ocultar um ecrã e seu respetivo objeto, atualizando o estado do objeto para 0 - oculto.
; Argumentos: R1 - endereço da variável que define o ecrã e estado do objeto (exibido/ocultado)
;			  R2 - número do ecrã a ser exibido/ocultado, em que o objeto está desenhado
; -------------------------------------------------------------------------------------------------------------------
esconde_objeto:
	PUSH R4
	MOV [ESCONDE_ECRA], R2				; Oculta o ecrã
	MOV R4, 0							; define o novo estado como "ocultado" (0)
	MOV [R1], R4						; atualiza estado do objeto para ocultado (0)
	POP R4
	RET

; -------------------------------------------------------------------------------------------------------------------
; ALTERNA_ENTRE_DOIS_OBJETOS: Rotina que alterna a exibição de dois ecrãs/objetos, consoante o ecrã exibido atualmente, e atualiza as flagas de estado dos objetos.
; Argumentos: R0 - endereço da variável que guarda o número do ecrã e estado do primeiro objeto
;			  R1 - endereço da variável que guarda o número do ecrã e estado do segundo objeto
; -------------------------------------------------------------------------------------------------------------------
alterna_entre_dois_objetos:
	PUSH R0
	PUSH R1
	PUSH R2
	PUSH R3
	PUSH R4
	PUSH R5
	PUSH R6
											
	MOV R2, [R0]							; Carrega o número do ecrã do primeiro objeto obtido a partir da variável de estado
	ADD R0, 2								; Avança para a próxima palavra da tabela, para obter o estado (0: oculto, 1: exibido)
	MOV R3, [R0]							; Carrega o estado do primeiro objeto

	MOV R4, [R1]							; Carrega o número do ecrã do segundo objeto obtido a partir da variável de estado
	ADD R1, 2								; Avança para a próxima palavra da tabela, para obter o estado (0: oculto, 1: exibido)
	MOV R5, [R1]							; Carrega o estado do segundo objeto

	CMP R3, 0								; Verifica se o primeiro objeto está oculto
	JZ mostra_obj_1							; Se sim, chama a rotina para exibir objeto 1
	JMP mostra_obj_2						; Se não, chama a rotina para exibir objeto 2

mostra_obj_1:							
	MOV [MOSTRA_ECRA], R2					; Comando para mostrar o ecrã do objeto 1
	MOV [ESCONDE_ECRA], R4					; Comando para esconder o ecrã do objeto 2
	MOV R6, 1								; Atualiza a flag para indicar que o primeiro objeto está exibido
	MOV [R0], R6							; Salva a flag atualizada
	MOV R6, 0								; Atualiza a flag para indicar que o segundo objeto está oculto
	MOV [R1], R6							; Salva a flag atualizada
	JMP fim_rotina_alterna_objetos			; Sai da rotina

mostra_obj_2:								
	MOV [ESCONDE_ECRA], R2					; Comando para esconder o ecrã do objeto 1
	MOV [MOSTRA_ECRA], R4					; Comando para mostrar o ecrã do objeto 2
	MOV R6, 0								; Atualiza a flag para indicar que o primeiro objeto está oculto
	MOV [R0], R6							; Salva a flag atualizada
	MOV R6, 1								; Atualiza a flag para indicar que o segundo objeto está exibido
	MOV [R1], R6							; Salva a flag atualizada
	JMP fim_rotina_alterna_objetos			; Sai da rotina

fim_rotina_alterna_objetos:					; Restaura os registros salvos e retorna à rotina que o chamou
	POP R6
	POP R5
	POP R4
	POP R3
	POP R2
	POP R1
	POP R0
	RET
	
; -------------------------------------------------------------------------------------------------------------------
; TECLADO - Rotina cooperativa que deteta quando se carrega numa tecla na 4ª linha
;		  do teclado. Não é bloqueante e retorna logo, haja ou não uma tecla carregada.
; -------------------------------------------------------------------------------------------------------------------
teclado:
	PUSH R0								; Salva o valor de R0 na pilha
	PUSH R1								; Salva o valor de R1 na pilha
	PUSH R2								; Salva o valor de R2 na pilha
	PUSH R3								; Salva o valor de R3 na pilha
	PUSH R5								; Salva o valor de R5 na pilha
	
	MOV  R2, TEC_LIN         			; endereço do periférico das linhas
	MOV  R3, TEC_COL         			; endereço do periférico das colunas
	MOV  R5, MASCARA		 			; para isolar os 4 bits de menor peso, ao ler as colunas do teclado

testa_linha_1:
	MOV  R1, 1				 			; testar a linha 1
	MOVB [R2], R1            			; escrever no periférico de saída (linhas)
	MOVB R0, [R3]            			; ler do periférico de entrada (colunas)
	AND  R0, R5				 			; elimina bits para além dos bits 0-3
	CMP  R0, 0               			; há tecla premida?
	JNZ  ha_tecla						; Se pressionada, salta para "ha_tecla" para guardar a tecla premida nas variaveis linha_carregada e coluna_carregada
	JMP testa_linha_2					; Se não, testa a próxima linha

testa_linha_2:
	MOV  R1, 2				 			; testar a linha 2 
	MOVB [R2], R1            			; escrever no periférico de saída (linhas)
	MOVB R0, [R3]            			; ler do periférico de entrada (colunas)
	AND  R0, R5				 			; elimina bits para além dos bits 0-3
	CMP  R0, 0               			; há tecla premida?
	JNZ  ha_tecla						; Se pressionada, salta para "ha_tecla" para guardar a tecla premida nas variaveis linha_carregada e coluna_carregada
	JMP testa_linha_3					; Se não, testa a próxima linha

testa_linha_3:
	MOV  R1, 4				 			; testar a linha 3 
	MOVB [R2], R1            			; escrever no periférico de saída (linhas)
	MOVB R0, [R3]            			; ler do periférico de entrada (colunas)
	AND  R0, R5				 			; elimina bits para além dos bits 0-3
	CMP  R0, 0               			; há tecla premida?
	JNZ  ha_tecla						; Se pressionada, salta para "ha_tecla" para guardar a tecla premida nas variaveis linha_carregada e coluna_carregada
	JMP testa_linha_4					; Se não, testa a linha 4

testa_linha_4:
	MOV  R1, 8				 			; testar a linha 4 
	MOVB [R2], R1            			; escrever no periférico de saída (linhas)
	MOVB R0, [R3]            			; ler do periférico de entrada (colunas)
	AND  R0, R5				 			; elimina bits para além dos bits 0-3
	CMP  R0, 0               			; há tecla premida?
	JNZ  ha_tecla						; Se pressionada, salta para "ha_tecla" para guardar a tecla premida nas variaveis linha_carregada e coluna_carregada

nao_ha_tecla:
	MOV  R1, 0				 			; nenhuma tecla premida na linha 4 - será guardada na variável linha_carregada
	MOV  R2, 0               			; nenhuma tecla premida na coluna - será guardada na variável coluna_carregada
	JMP sai_teclado

ha_tecla:
	MOV  R2, R0              			; houve uma tecla premida

sai_teclado:
	MOV [linha_carregada], R1			; atualiza na variável a informação sobre se houve ou não tecla premida (0 - nenhuma linha premida, 1, 2, 4, 8)
	MOV [coluna_carregada], R2			; atualiza na variável a informação sobre se houve ou não tecla premida (0 - nenhuma coluna premida, 1, 2, 4, 8)
										; O uso de R2 é redundante (bastava guardar R0), mas assim é mais explícito
	POP R5
    POP R3
    POP R2
    POP R1
    POP R0
    RET                      			; retorna sempre, haja ou não uma tecla carregada

; -------------------------------------------------------------------------------------------------------------------
; ACOES_TECLADO - Rotina que deteta qual tecla do teclado foi carregada e executa uma ação correspondente, podendo ativar
;			ou desativar animações, reproduzir som, selecionar imagem de fundo ou parar todos os sons, entre outras.
; Argumentos: Nenhum
; -------------------------------------------------------------------------------------------------------------------
acoes_teclado:
	PUSH R0								; Salva o valor de R0 na pilha
	PUSH R1								; Salva o valor de R1 na pilha
	PUSH R2								; Salva o valor de R2 na pilha

	MOV R0, [linha_carregada]			; obtém o valor da variável que guarda a tecla carregada (0 - nenhuma tecla carregada; 1, 2, 4 ou 8 - tecla carregada, e o valor indica a linha da tecla) 
	MOV R1, [coluna_carregada]			; obtém o valor da variável que guarda a tecla carregada (0 - nenhuma tecla carregada; 1, 2, 4 ou 8 - tecla carregada, e o valor indica a coluna da tecla) 
	CMP R0, 0							; Verifica se nenhuma tecla foi carregada (linha = 0)
	JZ sai_rotina_teclado				; Sai da rotina se nenhuma tecla foi pressionada

verifica_linha_1:
	CMP R0, 1							; Verifica se a linha carregada é a linha 1
	JNZ verifica_linha_2				; Se não for, vai para a verificação da linha 2
	CMP R1, 1							; Verifica se a coluna carregada é a coluna 1
	JZ ativa_interruptor_giftbox		; Faz a animação do giftbox
	CMP R1, 2							; Verifica se a coluna carregada é a coluna 2
	JZ ativa_interruptor_painatal		; Faz a animação do pai natal
	CMP R1, 4							; Verifica se a coluna carregada é a coluna 4
	JZ ativa_interruptor_arvore			; Faz a animação da arvore
	MOV R2, 8							; Prepara o valor 8 para comparação
	CMP R1, R2							; Verifica se a coluna carregada é a coluna 8
	JZ ativa_interruptor_merryxmas		; Faz a animação do merryxmas

verifica_linha_2:
	CMP R0, 2							; Verifica se a linha carregada é a linha 2
	JNZ verifica_linha_3				; Se não for, vai para a verificação da linha 3
	CMP R1, 1							; Verifica se a coluna carregada é a coluna 1
	JZ para_som							; Para todos os sons
	CMP R1, 2							; Verifica se a coluna carregada é a coluna 2
	JZ reproduz_som_0					; Reproduz o 1º som
	CMP R1, 4							; Verifica se a coluna carregada é a coluna 4
	JZ reproduz_som_1					; Reproduz o 2º som
	MOV R2, 8							; Prepara o valor 8 para comparação
	CMP R1, R2							; Verifica se a coluna carregada é a coluna 8
	JZ reproduz_som_2					; Reproduz o 3º som 

verifica_linha_3:
	CMP R0, 4							; Verifica se a linha carregada é a linha 3
	JNZ verifica_linha_4				; Se não for, vai para a verificação da linha 4
	CMP R1, 1							; Verifica se a coluna carregada é a coluna 1
	JZ seleciona_imagem_bg_0			; Seleciona a 1º imagem de fundo 
	CMP R1, 2							; Verifica se a coluna carregada é a coluna 2
	JZ seleciona_imagem_bg_1			; Seleciona a 2º imagem de fundo 
	CMP R1, 4							; Verifica se a coluna carregada é a coluna 4
	JZ seleciona_imagem_bg_2			; Seleciona a 3º imagem de fundo 
	MOV R2, 8							; Prepara o valor 8 para comparação
	CMP R1, R2							; Verifica se a coluna carregada é a coluna 8
	JZ seleciona_imagem_bg_3			; Seleciona a 4º imagem de fundo 

verifica_linha_4:
	MOV R2, 8							; aqui usa-se R2 para guardar o valor 8 (linha 4) porque não é possível fazer o CMP com k = 8
	CMP R0, R2							; verifica se nenhuma tecla foi premida na linha 4
	JNZ sai_rotina_teclado				; Se não for, sai da rotina
	CMP R1, 1							; verifica se tecla premida é C
	JZ liga_animacao_neve_e_arvore		; ativa a animação da árvore
	CMP R1, 2							; verifica se tecla premida é D
	JZ desliga_animacao_neve_e_arvore	; Desativa a animação da neve
	CMP R1, 4							; verifica se tecla premida é E
	JZ liga_animacao_merryxmas			; ativa a animação da árvore
	MOV R2, 8							; Prepara o valor 8 para comparação
	CMP R1, R2							; verifica se tecla premida é F
	JZ desliga_animacao_merryxmas			; Desativa a animação da árvore

; -------------------------------------------------------------------------------------------------------------------
; Ações a executar conforme tecla pressionada
