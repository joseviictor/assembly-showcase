	MOV [ESCONDE_ECRA], R7					; comando do MediaCenter para esconder o ecrã

desenhar_objeto_atual:
	CALL desenha_objeto						; Desenha o objeto atual. Argumentos: R4 - tabela que define o objeto a desenhar
	
