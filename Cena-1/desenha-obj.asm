REMOVE_BG	 			EQU COMANDOS + 40H 		; endereço do comando para remover background

SELECTIONA_MIDIA		EQU COMANDOS + 48H		; seleciona arquivo de midia (GIF, som ou video), para ser usado nos comandos subsequentes
