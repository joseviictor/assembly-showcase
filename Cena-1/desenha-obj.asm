desenha_objetos:
	PUSH R4
	PUSH R7
	PUSH R8									; Salva os registros usados para restaurar no final

seleciona_ecra: 
	MOV [SELECIONA_ECRA], R7				; seleciona ecrã (um para cada objeto)

