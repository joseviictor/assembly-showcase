	SUB  R1, 1
	MOV  [contador_atraso_neve], R1	; atualiza valor do contador do atraso
	JNZ  sai_atraso_neve
	MOV  R2, DELAY
	MOV  [contador_atraso_neve], R2	; volta a colocar o valor inicial no contador do atraso
